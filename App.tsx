export default function App() {
  const [selectedId, setSelectedId] = useState<string>(SONGS[0].id);
  const [isMenuOpen, setIsMenuOpen] = useState(false);

  const selectedSong = SONGS.find(s => s.id === selectedId) || SONGS[0];

  useEffect(() => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  }, [selectedId]);

  return (
    <div 
      className="min-h-screen bg-neutral-50 text-neutral-900 font-sans p-4 md:p-8"
      style={{ fontFamily: 'Arial, Helvetica, sans-serif' }}
    >
      {/* Mobile Header/Menu Toggle */}
      <div className="fixed top-0 left-0 right-0 bg-white border-b border-neutral-200 z-50 flex items-center justify-between px-6 py-4 md:hidden">
        <div className="flex items-center gap-2">
          <Music className="w-6 h-6 text-indigo-600" />
          <span className="font-bold text-lg">Сборник песен</span>
        </div>
        <button 
          onClick={() => setIsMenuOpen(!isMenuOpen)}
          className="p-2 hover:bg-neutral-100 rounded-lg transition-colors"
          id="menu-toggle"
        >
          {isMenuOpen ? <X className="w-6 h-6" /> : <Menu className="w-6 h-6" />}
        </button>
      </div>

      {/* Desktop Sidebar Navigation */}
      <aside className="hidden md:block fixed left-0 top-0 bottom-0 w-72 bg-white border-r border-neutral-200 p-8 overflow-y-auto">
        <div className="flex items-center gap-3 mb-12">
          <Music className="w-8 h-8 text-indigo-600" />
          <h1 className="text-2xl font-bold tracking-tight">Музыкальный Сборник</h1>
        </div>
        
        <nav className="space-y-2">
          <p className="text-[10px] uppercase tracking-widest text-neutral-400 font-bold mb-4 px-2">Выберите песню</p>
          {SONGS.map((song) => (
            <button
              key={song.id}
              onClick={() => setSelectedId(song.id)}
              id={`nav-desktop-${song.id}`}
              className={`w-full text-left px-4 py-3 rounded-xl transition-all duration-200 ${
                selectedId === song.id 
                ? 'bg-indigo-600 text-white shadow-lg shadow-indigo-200 scale-[1.02]' 
                : 'hover:bg-neutral-100 text-neutral-600'
              }`}
            >
              <div className="font-bold text-lg">{song.title}</div>
              <div className={`text-[11px] opacity-80 ${selectedId === song.id ? 'text-indigo-100' : 'text-neutral-400'}`}>
                {song.style}
              </div>
            </button>
          ))}
        </nav>
      </aside>

      {/* Mobile Drawer Menu */}
      <AnimatePresence>
        {isMenuOpen && (
          <motion.div
            initial={{ opacity: 0, x: -100 }}
            animate={{ opacity: 1, x: 0 }}
            exit={{ opacity: 0, x: -100 }}
            className="fixed inset-0 bg-white z-[60] p-8 pt-24 md:hidden overflow-y-auto"
          >
            <nav className="space-y-4">
              {SONGS.map((song) => (
                <button
                  key={song.id}
                  onClick={() => {
                    setSelectedId(song.id);
                    setIsMenuOpen(false);
                  }}
                  id={`nav-mobile-${song.id}`}
                  className={`w-full text-left p-6 rounded-2xl border-2 transition-all ${
                    selectedId === song.id 
                    ? 'border-indigo-600 bg-indigo-50 text-indigo-900' 
                    : 'border-neutral-100 hover:border-indigo-200'
                  }`}
                >
                  <div className="text-2xl font-bold mb-1">{song.title}</div>
                  <div className="text-sm text-neutral-500 uppercase tracking-wider">{song.style}</div>
                </button>
              ))}
            </nav>
          </motion.div>
        )}
      </AnimatePresence>

      {/* Main Content Area */}
      <main className="md:ml-72 pt-20 md:pt-4 max-w-4xl mx-auto">
        <motion.div
          key={selectedId}
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.4 }}
          className="bg-white rounded-3xl p-8 md:p-16 shadow-sm border border-neutral-100"
          id="song-content"
        >
          <div className="mb-12 border-b border-neutral-100 pb-8">
            <div className="inline-block px-4 py-1.5 bg-indigo-50 text-indigo-600 rounded-full text-sm font-bold uppercase tracking-widest mb-6">
              {selectedSong.style}
            </div>
            <h2 className="text-6xl md:text-8xl font-black text-neutral-900 leading-tight mb-4 tracking-tighter">
              {selectedSong.title}
            </h2>
          </div>

          <div 
            className="whitespace-pre-wrap text-2xl md:text-4xl text-neutral-700 leading-relaxed font-normal"
            style={{ fontSize: 'clamp(1.5rem, 4vw, 2.5rem)' }}
          >
            {selectedSong.lyrics}
          </div>

          <div className="mt-24 pt-12 border-t border-neutral-100 flex flex-col items-center justify-center text-neutral-300">
            <ChevronDown className="w-8 h-8 animate-bounce mb-4" />
            <p className="text-sm uppercase tracking-widest font-bold">Конец текста</p>
          </div>
        </motion.div>
      </main>

      {/* Background Decor */}
      <div className="fixed top-0 right-0 -z-10 w-1/3 h-1/3 bg-indigo-50/50 rounded-full blur-3xl" />
      <div className="fixed bottom-0 left-0 -z-10 w-1/4 h-1/4 bg-neutral-100 rounded-full blur-3xl" />
    </div>
  );
}
